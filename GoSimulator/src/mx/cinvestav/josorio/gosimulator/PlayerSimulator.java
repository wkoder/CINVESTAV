package mx.cinvestav.josorio.gosimulator;

/**
 * Different kind of player simulators.
 * @author Moises Osorio
 */
public enum PlayerSimulator {

	HUMAN("Human"),
	RANDOM("Random"),
	SMART("Smart"),
	GNUGO("GNU Go");
	
	private String desc;
	
	private PlayerSimulator(String desc) {
		this.desc = desc;
	}
	
	@Override
	public String toString() {
		return desc;
	}
	
}
